            <li>- Прозрачность. В системе хранится информация по абсолютно всем транзакциям, а называется история операций (как вы уже знаете) – блокчейн. Любой человек может по адресу биткоинов посмотреть, сколько на вашем счету. Однако без имени, информация все же остается анонимной.</li>
            <li>- Быстрый перевод. Криптовалюту можно отправить в любую точку мира, при этом перевод займет несколько минут после обработки платежа.</li>
